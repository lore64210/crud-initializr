CREATE TABLE IF NOT EXISTS lore(
	id BIGINT AUTO_INCREMENT PRIMARY KEY,
	fecha_creacion DATETIME,
	nombre VARCHAR(255),
	edad INT
	);

CREATE TABLE IF NOT EXISTS casa(
	id BIGINT AUTO_INCREMENT PRIMARY KEY,
	fecha_creacion DATETIME,
	nose_id BIGINT,
	CONSTRAINT fk_nose FOREIGN KEY (nose_id) REFERENCES nose(id)
	);

